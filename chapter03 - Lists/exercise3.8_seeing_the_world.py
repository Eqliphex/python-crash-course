locations = ['Nepal', 'Japan', 'Antarktis', 'Switzerland', 'Russia']

#  Raw output
print(locations)

#  Temp sorted
print(sorted(locations))

#  Still in original form
print(locations)

#  Reversing the list
locations.reverse()
print(locations)

locations.reverse()
print(locations)

#  Sorting the list permanently
locations.sort()
print(locations)

locations.sort(reverse=True)
print(locations)

