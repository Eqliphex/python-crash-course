cities = ['Singapore', 'Beijing', 'Copenhagen', 'Tokyo', 'Paris', 'Amsterdam']

print("Original list:")
print(cities)

print()

print("Temporary sorting the list:")
print(sorted(cities))

print()

print("Original List:")
print(cities)

print()

print("Reversing the list:")
cities.reverse()
print(cities)

print()

print("Reverse Back:")
cities.reverse()
print(cities)

print()

print("Permanent sorting list:")
cities.sort()
print(cities)

print("Length of the list:")
print(len(cities))

print()

print("Popped one from list:")
cities.pop()
print(cities)